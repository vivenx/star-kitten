class EndingSceneModel:
    """Хранит кадры и текущее состояние финальной истории."""
    FRAMES = (
        (
            "После долгого пути котенок наконец нашел упавшую звезду.\n"
            "Ее свет был слабым, но чистым."
        ),
        (
            "Котенок прикоснулся к звезде и попросил спасти друга.\n"
            "Свет звезды очистил его от темной болезни."
        ),
        (
            "Вернувшись в деревню, котенок увидел друга здоровым.\n"
            "Его свет снова сиял как прежде."
        ),
        (
            "Но тьма все еще пожирала мир.\n"
            "Слишком много существ продолжали страдать.\n\n"
            "Теперь его цель стала больше.\n"
            "Не просто спасать близких... а уничтожить тьму навсегда."
        ),
        (
            "С мечом в лапах и светом звезды в сердце\n"
            "котенок отправился дальше в неизвестность..."
        ),
    )

    def __init__(self):
        """Создаёт модель финальной сцены с первого кадра."""
        self.frame_index = 0

    @property
    def text(self):
        """Возвращает текст текущего кадра."""
        return self.FRAMES[self.frame_index]

    @property
    def is_last_frame(self):
        """Проверяет, является ли текущий кадр последним."""
        return self.frame_index == len(self.FRAMES) - 1

    def advance(self):
        """Переходит к следующему кадру и сообщает результат перехода."""
        if self.is_last_frame:
            return False
        self.frame_index += 1
        return True
